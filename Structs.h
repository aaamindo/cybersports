// =====================================================


// ---------------------------------------------------
// Структура: Аккаунт пользователя
// Хранится в бинарном файле accounts.bin
// ---------------------------------------------------
struct Account
{
    int    id;          // уникальный ID аккаунта
    char   login[30];   // логин пользователя
    char   password[30];// пароль
    char   role[15];    // роль: "admin", "organizer", "player"
    bool   isBanned;    // заблокирован ли аккаунт
};

// ---------------------------------------------------
// Структура: Игрок
// Хранится в текстовом файле players.txt
// ---------------------------------------------------
struct Player
{
    int    id;           // уникальный ID игрока
    int    accountId;    // ссылка на аккаунт (Account.id)
    char   nickname[30]; // игровой псевдоним
    char   fullName[100];// ФИО игрока
    int    age;          // возраст (минимум 14 лет)
    char   rank[20];     // соревновательный ранг
    double winrate;      // процент побед (0.0 - 100.0)
    double kda;          // коэффициент эффективности (kills+assists)/deaths
    int    totalGames;   // всего сыграно игр
    int    totalWins;    // всего побед
    int    totalKills;   // всего убийств
    int    totalDeaths;  // всего смертей
    int    totalAssists; // всего помощей
};

// ---------------------------------------------------
// Структура: Организатор
// Хранится в текстовом файле organizers.txt
// ---------------------------------------------------
struct Organizer
{
    int    id;           // уникальный ID организатора
    int    accountId;    // ссылка на аккаунт (Account.id)
    char   orgName[50];  // название организации или ФИО
    char   contacts[50]; // контактные данные
    double trustRating;  // рейтинг доверия (0.0 - 10.0)
    int    eventsCount;  // количество проведённых мероприятий
};

// ---------------------------------------------------
// Структура: Турнир
// Хранится в текстовом файле tournaments.txt
// ---------------------------------------------------
struct Tournament
{
    int    id;            // уникальный ID турнира
    int    organizerId;   // ID организатора который создал турнир
    char   title[50];     // название турнира
    char   discipline[30];// игровая дисциплина (CS2, Dota 2, Valorant)
    char   format[10];    // формат матчей (1x1, 2x2, 5x5)
    double prizePool;     // размер призового фонда
    char   date[15];      // дата проведения (дд.мм.гггг)
    char   status[15];    // статус: "Регистрация", "Активен", "Завершён"
    int    maxPlayers;    // максимальное количество участников
    int    currentPlayers;// текущее количество зарегистрированных участников
};

// ---------------------------------------------------
// Структура: Команда
// Хранится в текстовом файле teams.txt
// ---------------------------------------------------
struct Team
{
    int    id;           // уникальный ID команды
    char   name[50];     // название команды
    int    captainId;    // ID капитана (Player.id)
    int    tournamentId; // ID турнира, в котором участвует команда
    int    memberIds[5]; // ID участников команды (максимум 5 человек)
    int    memberCount;  // фактическое количество участников
};

// ---------------------------------------------------
// Структура: Заявка на участие в турнире
// Хранится в текстовом файле applications.txt
// ---------------------------------------------------
struct TournamentApplication
{
    int    id;           // уникальный ID заявки
    int    tournamentId; // ID турнира на который подана заявка
    int    playerId;     // ID игрока подавшего заявку
    int    teamId;       // ID команды (0 если одиночная заявка)
    char   status[15];   // статус: "Ожидание", "Одобрено", "Отклонено"
    char   date[15];     // дата подачи заявки
};

// ---------------------------------------------------
// Структура: Матч
// Хранится в текстовом файле history.txt
// ---------------------------------------------------
struct Match
{
    int    id;           // уникальный ID матча
    int    tournamentId; // ID турнира
    int    player1Id;    // ID первой команды (или участника)
    int    player2Id;    // ID второй команды (или участника)
    int    score1;       // счёт первой команды (или участника)
    int    score2;       // счёт второй команды (или участника)
    int    winnerId;     // ID победителя
    char   date[15];     // дата проведения матча
    char   status[20];      // "Запланирован" / "Завершён"
};

// ---------------------------------------------------
// Структура: Запись в чёрном списке
// Хранится в текстовом файле blacklist.txt
// ---------------------------------------------------
struct Ban
{
    int    id;           // уникальный ID записи о бане
    int    targetId;     // ID заблокированного игрока (Player.id)
    int    issuedBy;     // ID организатора который выдал бан
    char   reason[100];  // причина блокировки
    char   expireDate[15];// дата окончания блокировки (дд.мм.гггг)
    bool   isActive;     // активна ли блокировка в данный момент
};
// ---------------------------------------------------
// Структура: Дисциплина
// Хранится в текстовом файле discipline.txt
// ---------------------------------------------------
struct Discipline
{
    int  id;        // уникальный ID
    char name[50];  // название: "Dota 2", "CS2", "Valorant"...
};
// Заявка на участие в турнире
struct TourApplication
{
    int    id;
    int    playerId;       // кто подал
    int    tournamentId;   // на какой турнир
    char   submitDate[15]; // дата подачи
    char   status[20];     // "Ожидает", "Одобрена", "Отклонена"
    char   rejectReason[100]; // причина отклонения если есть
};

// Жалоба
struct Complaint
{
    int    id;
    int    targetPlayerId;    // на кого жалоба
    int    tournamentId;      // в рамках какого турнира
    int    issuedBy;          // кто подал (playerId)
    char   reason[200];       // причина
    char   status[20];        // "Новая", "Принята", "Отклонена"
    char   sanction[30];      // "Предупреждение", "Бан", "Дисквалификация"
};

// Уведомление
struct Notification
{
    int    id;
    int    targetPlayerId;  // кому
    char   message[200];    // текст
    char   date[15];        // дата
    bool   isRead;          // прочитано или нет
};